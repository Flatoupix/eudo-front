<template>
  <div>
    <v-text-field
      v-model="content"
      v-if="!linkMode"
      @blur="isMimic"
      @update:error="isReady($event)"
      type="tel"
      v-bind="$attrs"
      :rules="rules"
    >
      <template v-slot:append v-if="$attrs.tooltip">
        <v-tooltip top>
          <template v-slot:activator="{ on }">
            <v-icon v-on="on" style="cursor: pointer">mdi-help-circle-outline</v-icon>
            <v-icon v-on="on" style="cursor: pointer">mdi-help-circle-outline</v-icon>
          </template>
          {{ $attrs.tooltip }}
        </v-tooltip>
      </template>
    </v-text-field>
    <edn-goto
      v-else
      @edit="
        exit($event)
        getFocus
      "
      linkMode="phone"
      :label="$attrs.label"
    >
      {{ content }}
    </edn-goto>
  </div>
</template>

<script>
import VueTheMask from 'vue-the-mask'
import Vue from 'vue'

import { ednRequired } from './mixins/ednRequired'
import { ednVModel } from './mixins/ednVModel'

Vue.use(VueTheMask)

export default {
  props: {
    irisMimic: {
      type: Boolean,
      default: () => false,
    },
    invalidPhoneMsg: {
      type: String,
      default: () => 'Vous devez entrer un numéro de téléphone valide.',
    },
    mask: {
      type: String,
      default: () => '##.##.##.##.##',
    },
  },
   data() {
    return {
      linkContent: '',
      readyToConvert: null,
      linkMode: false,
    }
  },
    methods: {
    changeToLink() {
      this.linkContent = this.content
    },
    isReady(e) {
      this.readyToConvert = !e
    },
    isMimic() {
      this.linkMode = this.irisMimic && this.content.length > 0 && this.readyToConvert
    },
    exit(e) {
       this.linkMode = e
    },
  },
  mounted() {
    this.rules.push((v) => /^[/\-.+()# 0-9]*$/.test(v) || this.invalidPhoneMsg)
  },
   computed: {
    getFocus() {
      return this.linkMode ? '':setTimeout(()=>this.$refs.edn_mail.focus())
    }
  },
  inheritAttrs: false,
  mixins: [ednRequired, ednVModel],
}
</script>
